public class While_1 {
  public static void main(String[] args) {
    int num = 0;

    //num이 5보다 작은 동안 반복
    while(num < 5){
      System.out.println(1);
      System.out.println(2);
      num++; //++num; num +=1; num = num+1;
    }
    System.out.println();
    /// /////////////////////////////////////////////
    for( int i = 0; i < 5; i++ ){
      System.out.println(1);
      System.out.println(2);
    }



  }
}
